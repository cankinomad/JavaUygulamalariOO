package com.berka.hafta02.gun05;


public class Soru4 {

	public static void main(String[] args) {
		
				
		int carpim;
		for (int i = 1; i <= 10; i++) {
			for (int j = 1; j <= 9; j++) {
				carpim=i*j;
				System.out.println(i+"x"+j+"="+carpim);
			}
			System.out.println("----------------");
		
			
		}
	}

}
